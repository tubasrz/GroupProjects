package com.syntax.reviewclass2;

public class ShortHandOperator {

	public static void main(String[] args) {
		
		int number=10;
		System.out.println(++number); //önce topla 11
		System.out.println(number++);  //önce rakam sonra +1=yazan rakam olacak=11
		System.out.println(number);    //rakam 11i +1 ile toplayınca number=12 olmuştu. output 12 oldu
		
	
}
}