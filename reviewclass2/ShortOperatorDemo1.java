package com.syntax.reviewclass2;

public class ShortOperatorDemo1 {

	public static void main(String[] args) {
		
		int number=10;
		number=number+1;
		System.out.println(number);
		
		number++;
		System.out.println(number);

		number+=10;     //number=number+10
		System.out.println(number);
		
		number*=100;
		System.out.println(number);
		
		number=10;
		++number;
		System.out.println(number);
		
		number++;
		System.out.println(number);
		
	}

}
